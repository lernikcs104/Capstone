    public static final String[] descriptions = new String[] {
            "Assessments,Doctors,and Dentists",
            "Therapies", "Financial, Legal,and Health Insurance Resources", "Support","Social Recriation",
            "educational programs and Services" , "Our Services" };

    public static final Integer[] images = { R.drawable.medicalrecords,
            R.drawable.healthcare, R.drawable.balance, R.drawable.charity,
            R.drawable.americanfootball, R.drawable.library, R.drawable.suitcase, R.drawable.team };


    ListView listView;
    List<Services> rowItems;

    /** Called when the activity is first created. */
    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        //creating toolbar to place logo
        Toolbar toolbar = (Toolbar) findViewById(R.id.my_toolbar);
        setSupportActionBar(toolbar);
        //getSupportActionBar().setLogo(R.drawable.logo1);


        RES_REQUEST=0;
        rowItems = new ArrayList<Services>();
        for (int i = 0; i < descriptions.length; i++) {
            Services item = new Services(images[i],  descriptions[i]);
            rowItems.add(item);
        }

        listView = (ListView) findViewById(R.id.list);
        ListArrayAdapter adapter = new ListArrayAdapter(this,
                R.layout.list_item, rowItems);
        listView.setAdapter(adapter);
        listView.setOnItemClickListener(this);
    }

    @Override
    public void onItemClick(AdapterView<?> parent, View view, int position,
                            long id) {

        Toast toast = Toast.makeText(getApplicationContext(),
                "Item " + (position + 1) + ": " + rowItems.get(position),
                Toast.LENGTH_SHORT);
        toast.setGravity(Gravity.BOTTOM|Gravity.CENTER_HORIZONTAL, 0, 0);
        toast.show();

        Intent intent = new Intent(getApplicationContext(), ResourcesActivity.class);

        startActivityForResult(intent, RES_REQUEST);
    }




